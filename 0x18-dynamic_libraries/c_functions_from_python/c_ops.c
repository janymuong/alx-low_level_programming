/**
  * add - add two integers
  * @a: first interger arg
  * @b: second arg
  *
  * Return: c_ops result
  */
int add(int a, int b)
{
	return (a + b);
}

/**
* sub - subtraction op for two integers
* @a: first interger arg
* @b: second arg
*
* Return: c_ops result
*/
int sub(int a, int b)
{
	return (a - b);
}

/**
* mul - multiply integers
* @a: first interger arg
* @b: second arg
*
* Return: c_ops result
*/
int mul(int a, int b)
{
	return (a * b);
}

/**
* div - divsion two integers
* @a: first interger arg
* @b: second arg
*
* Return: c_ops result
*/
int div(int a, int b)
{
	return (a / b);
}

/**
* mod - modulus two integers
* @a: first interger arg
* @b: second arg
*
* Return: c_ops result
*/
int mod(int a, int b)
{
	return (a % b);
}
